public class datatypes {
    public static void main(String[] args){

    // whole numbers
        // byte
        byte dtypebyte =127;
        System.out.println(dtypebyte);
        System.out.println(Byte.MIN_VALUE); 
        System.out.println(Byte.MAX_VALUE);        
        // short
        short dtypeshort = 32767;
        System.out.println(dtypeshort);
        System.out.println(Short.MIN_VALUE); 
        System.out.println(Short.MAX_VALUE);  
        // int
        int dtypeint = 2147483647;
        System.out.println(dtypeint);
        System.out.println(Integer.MIN_VALUE); 
        System.out.println(Integer.MAX_VALUE);  
        // Long
        long dtypelong =2147483649l;
        System.out.println(dtypelong);
        System.out.println(Long.MIN_VALUE); 
        System.out.println(Long.MAX_VALUE);
        System.out.println("----------------------");

        
    // decimal numbers
        // float
        float dtypefloat = 1.1234567f;
        System.out.println(dtypefloat);
        System.out.println(Float.MIN_VALUE); 
        System.out.println(Float.MAX_VALUE);
        // double
        double dtypedouble = 1.123456789123456;
        System.out.println(dtypedouble);
        System.out.println(Double.MIN_VALUE); 
        System.out.println(Double.MAX_VALUE);
    // characters
        //char
        char Dtypechar='y';
        System.out.println(Dtypechar);
        System.out.println((int) Dtypechar);
        System.out.println((int)Character.MIN_VALUE); 
        System.out.println((int)Character.MAX_VALUE);
        System.out.println(Double.MAX_VALUE);
    // booleans
        //boolean
        boolean dtypeboolean=false;
        System.out.println(dtypeboolean);
        dtypeboolean=true;
        System.out.println(dtypeboolean);

    }        
}
